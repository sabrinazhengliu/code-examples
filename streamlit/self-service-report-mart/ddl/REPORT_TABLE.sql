create or replace TRANSIENT TABLE REPORT_TABLE (
	REPORT_ID NUMBER(38,0) autoincrement start 1 increment 1 noorder,
	REPORT_NAME VARCHAR(255),
	DESCRIPTION VARCHAR(16777216),
	CREATED_DATE DATE,
	UPDATED_TIMESTAMP TIMESTAMP_NTZ(9),
	SUBMITTED_AT TIMESTAMP_LTZ(9),
	IS_ACTIVE BOOLEAN,
	STATUS VARCHAR(50),
	PRIORITY NUMBER(2,0),
	CATEGORY VARCHAR(100),
	REGION VARCHAR(50),
	COUNTRY_CODE VARCHAR(3),
	DEPARTMENT VARCHAR(100),
	ASSIGNED_TO VARCHAR(255),
	CUSTOMER_EMAIL VARCHAR(320),
	REVENUE NUMBER(18,2),
	COST NUMBER(18,2),
	DISCOUNT_PERCENT FLOAT,
	QUANTITY NUMBER(38,0),
	UNIT_PRICE NUMBER(12,4),
	TAX_RATE FLOAT,
	TOTAL_AMOUNT NUMBER(18,2),
	RATING NUMBER(38,0),
	WEIGHT_KG FLOAT,
	NOTES VARCHAR(16777216),
	TAGS ARRAY,
	METADATA VARIANT,
	ADDRESS OBJECT,
	START_TIME TIME(9),
	BINARY_ATTACHMENT BINARY(8388608)
);
